"""Rekordbox cue ColorTableIndex -> RGB.

DjmdCue.ColorTableIndex is an index into rekordbox's built-in cue color palette
(distinct from the 8 named *track* colors in DjmdColor, which carry no RGB). The
palette below is the community-reverse-engineered table from Deep-Symmetry's
beat-link project (issue #51). Indices > 0x3E render black in the rekordbox UI.

Because Traktor Pro 4 stores arbitrary #RRGGBB per cue, these map across exactly.
"""

