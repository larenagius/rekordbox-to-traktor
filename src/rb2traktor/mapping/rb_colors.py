def color_index_to_rgb(index: Optional[int]) -> Optional[tuple[int, int, int]]:
    """Map a rekordbox ColorTableIndex to RGB, or None if absent/uncolored."""
    if index is None or index < 0:
        return None
    return RB_CUE_COLORS.get(index)
